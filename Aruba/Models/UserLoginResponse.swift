import Foundation

struct UserLoginResponse: Codable {
    let codRetorno: Int
    let message: JSONNull?
    let requestStatus: String
    let sesion: Sesion
}

struct Sesion: Codable {
    let id: Int
    let usuario: Usuario
    let token, fechaHoraCreacion, fechaHoraExpiracion: String
    let active: Bool
}

struct Usuario: Codable {
    let id: Int
    let rol: Rol
    let nombres, apellidos: String
    let ubicaciones: [AAddress]
    let email: String
    let cel: JSONNull?
    let fechaHoraRegistro: String
    let facebookUser, active: Bool
}

struct Rol: Codable {
    let id: Int
    let nombre: String
    let permissionLevel: Int
    let active: Bool
}

struct AAddress: Codable {
    let id: Int
    let nombre: String?
    let calle1, calle2, numero, referencias: String
    let latitud, longitud: Double
    let active: Bool
}

// MARK: Convenience initializers

extension UserLoginResponse {
    init?(data: Data) {
        guard let me = try? JSONDecoder().decode(UserLoginResponse.self, from: data) else { return nil }
        self = me
    }

    init?(_ json: String, using encoding: String.Encoding = .utf8) {
        guard let data = json.data(using: encoding) else { return nil }
        self.init(data: data)
    }

    init?(fromURL url: String) {
        guard let url = URL(string: url) else { return nil }
        guard let data = try? Data(contentsOf: url) else { return nil }
        self.init(data: data)
    }

    var jsonData: Data? {
        return try? JSONEncoder().encode(self)
    }

    var json: String? {
        guard let data = self.jsonData else { return nil }
        return String(data: data, encoding: .utf8)
    }
}

